
s1 = input("دخل يسطا ام القيمه الاوله ومتزهقنيش :").lower()
s2 = input("خلاص متزعلش مش هتعصب عليك تاني دخل القيمه التانايه بقي :").lower()

if s1 < s2:
    print(-1)
    print("الكلمه الاوله اقل من الكلمه التانيه")     
elif s1 > s2:
    print(1)
    print("الكلمه الاوله اكبر من الكلمه التانيه")
else:
    print(0)
    print("الكلام ال انت مدخله مش شبه بعضه يسطا")